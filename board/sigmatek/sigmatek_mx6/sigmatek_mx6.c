{
	gd->ram_size = CONFIG_DDR_MB * SZ_1M;

	return 0;
}

int board_mmc_getcd(struct mmc *mmc)
{
	struct fsl_esdhc_cfg *cfg = (struct fsl_esdhc_cfg *)mmc->priv;
	int ret;

	if (cfg->esdhc_base == USDHC1_BASE_ADDR) {
		gpio_direction_input(IMX_GPIO_NR(1, 2));
		ret = !gpio_get_value(IMX_GPIO_NR(1, 2));
	} else {
		gpio_direction_input(IMX_GPIO_NR(3, 9));
		ret = !gpio_get_value(IMX_GPIO_NR(3, 9));
	}

	return ret;
};

int board_mmc_init(bd_t *bis)
{
	s32 status = 0;	
	
	imx_iomux_v3_setup_multiple_pads(usdhc1_pads, ARRAY_SIZE(usdhc1_pads));
	status |= fsl_esdhc_initialize(bis, &usdhc_cfg[0]);

	imx_iomux_v3_setup_multiple_pads(usdhc3_pads, ARRAY_SIZE(usdhc3_pads));
	status |= fsl_esdhc_initialize(bis, &usdhc_cfg[1]);

	return status;
}

static int mx6_rgmii_rework(struct phy_device *phydev)
{
	unsigned short val;

	/* To enable AR8031 ouput a 125MHz clk from CLK_25M */
	phy_write(phydev, MDIO_DEVAD_NONE, 0xd, 0x7);
	phy_write(phydev, MDIO_DEVAD_NONE, 0xe, 0x8016);
	phy_write(phydev, MDIO_DEVAD_NONE, 0xd, 0x4007);

	val = phy_read(phydev, MDIO_DEVAD_NONE, 0xe);
	val &= 0xffe3;
	val |= 0x18;
	phy_write(phydev, MDIO_DEVAD_NONE, 0xe, val);

	/* introduce tx clock delay */
	phy_write(phydev, MDIO_DEVAD_NONE, 0x1d, 0x5);
	val = phy_read(phydev, MDIO_DEVAD_NONE, 0x1e);
	val |= 0x0100;
	phy_write(phydev, MDIO_DEVAD_NONE, 0x1e, val);

	return 0;
}

int board_phy_config(struct phy_device *phydev)
{
	mx6_rgmii_rework(phydev);

	if (phydev->drv->config)
		phydev->drv->config(phydev);

	return 0;
}

static int i2c_read_mac(uchar *buffer)
{
	if (i2c_read(CONFIG_SYS_I2C_EEPROM_ADDR, 0x33,
			CONFIG_SYS_I2C_EEPROM_ADDR_LEN, buffer, 6)) {
		puts("eeprom: read failed\n");
		return -1;
	}
	printf("eeprom: read mac %pM\n", buffer);
	return 0;
}

int board_eth_init(bd_t *bis)
{
	int ret;
	unsigned char mac_addr[6];

	if (!eth_getenv_enetaddr("ethaddr", mac_addr) && !i2c_read_mac(mac_addr))
		eth_setenv_enetaddr("ethaddr", mac_addr);

	setup_iomux_enet();

	ret = cpu_eth_init(bis);
	if (ret)
		printf("FEC MXC: %s:failed\n", __func__);

	return 0;
}

